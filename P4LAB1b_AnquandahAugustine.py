import turtle
# A
t=turtle.Turtle()
t.penup()
t.goto(120,50) #centering in the screen
t.pendown()
t.pensize(10)
t.pencolor("red")
 
t.right(65)
t.forward(100)
 
t.setpos(120,50)
t.right(50)
t.forward(100)
 
t.penup()
t.setpos(100,-10)
t.right(65)
t.pendown()
t.backward(50)

# Hide turtle
t.hideturtle()
